package adminModel;

public class CarModel {
	private int idCar;
	private String nameCar;
	private String brand;
	private int quanitySeat;
	private int gear;
	private int fuel;
	private int price;
	private String imageTitle;
	private int quantityCar;
	private String content;
	public int getIdCar() {
		return idCar;
	}
	public void setIdCar(int idCar) {
		this.idCar = idCar;
	}
	public String getNameCar() {
		return nameCar;
	}
	public void setNameCar(String nameCar) {
		this.nameCar = nameCar;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getQuanitySeat() {
		return quanitySeat;
	}
	public void setQuanitySeat(int quanitySeat) {
		this.quanitySeat = quanitySeat;
	}
	public int getGear() {
		return gear;
	}
	public void setGear(int gear) {
		this.gear = gear;
	}
	public int getFuel() {
		return fuel;
	}
	public void setFuel(int fuel) {
		this.fuel = fuel;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImageTitle() {
		return imageTitle;
	}
	public void setImageTitle(String imageTitle) {
		this.imageTitle = imageTitle;
	}
	public int getQuantityCar() {
		return quantityCar;
	}
	public void setQuantityCar(int quantityCar) {
		this.quantityCar = quantityCar;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
	

}
